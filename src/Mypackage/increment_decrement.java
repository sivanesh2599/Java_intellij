package Mypackage;

public class increment_decrement {
    public static void main(String[] args) {
        int i=11;
        //pre increment
        System.out.println(++i);
        System.out.println(i);
        //post increment
        System.out.println(i++);
        System.out.println(i);
        //pre decrement
        System.out.println(--i);
        System.out.println(i);
        //post decrement
        System.out.println(i--);
        System.out.println(i);

    }
}
